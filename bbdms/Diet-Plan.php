        <section>
            <h2>Nutrition Tips Before and After Donating Blood</h2>
            <ol>
                <li>Add more plant-based iron-rich foods: Keeping a healthy level of iron is always vital, especially when donating blood.</li>
                <li>Avoid iron-blockers: Don’t let all that hard work from those iron-rich foods go to waste! Just like Vitamin C helps you absorb iron in food, there are also foods that block iron absorption.</li>
                <li>Steer clear of fatty foods: Skip fatty foods before your blood donation as well to avoid feeling sluggish afterward and possibly affecting your blood test results.</li>
                <li>Drink extra fluids: It’s always important to stay hydrated, even more so before and after you donate blood since most of your blood is made up of water.</li>
