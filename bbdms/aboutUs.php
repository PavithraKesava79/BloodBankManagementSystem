            <h2>Eligibility Criteria for Blood Donation</h2>
            <ol>
                <li><strong>Age Limit:</strong> The age limit for blood donation typically ranges between 18 and 60 years.</li>
                <li><strong>Weight Criteria:</strong> Your minimum weight for donating blood must be 45 kg.</li>
                <li><strong>Health and Physical Criteria:</strong>
                    <ul>
                        <li>You must be physically fit to donate blood.</li>
                        <li>You cannot donate if you have any flu, cold, sore throat, fever, stomach flu, or any type of infection.</li>
                        <li>Waiting periods may apply if you have a recent piercing, tattoo, rabies treatment, or Hepatitis B vaccination.</li>
                        <li>Your hemoglobin level must not be lower than 12.5 g/dl.</li>
                        <li>Your pulse rate should be 50-100/minute.</li>
                        <li>Blood pressure levels must be within the ranges of Diastolic 50-100 mm Hg and Systolic 100-180 mm Hg.</li>
                        <li>Waiting periods may be required after dental procedures, alcohol consumption, and taking certain medications.</li>
                    </ul>
                </li>
                <li><strong>Body Temperature:</strong>
                    <ul>
                        <li>Your body temperature must be normal at the time of donation.</li>
                        <li>The temperature of your mouth must not exceed 37.5 degrees Celsius.</li>
                    </ul>
                </li>
            </ol>
        </section>

        <section>
            <h2>Who Cannot Donate Blood</h2>
            <ul>
                <li>Several conditions may result in a temporary or permanent deferral, including:</li>
                <li>Diabetes, chest pain, high blood pressure, or heart disease.</li>
                <li>Blood clotting disorders or blood diseases.</li>
                <li>Cancer, white patches in the mouth, or swollen lymph nodes in armpits, groin, or neck.</li>
                <li>Unexplained fever, weight loss, fatigue, or night sweats.</li>
                <li>Menstruating women are often not allowed to donate blood.</li>
                <li>Testing positive for HIV or AIDS.</li>
                <li>Pregnancy or breastfeeding.</li>
            </ul>
        </section>

        <section>
            <h2>Things to Consider Before Donating Blood</h2>
            <ul>
                <li>Inform the donation center about your medications and dosages.</li>
                <li>Low iron levels may disqualify you from blood donation, so it's important to be aware of your iron levels.</li>
                <li>Rules for blood donation may have gender-specific differences, such as the interval time between two donations, which is often longer for females than males.</li>
            </ul>
